public class Guest {

    String guestName;
    String guestContact;


    public Guest(String guestName, String guestContact){
        this.guestName = guestName;
        this.guestContact = guestContact;
    }

    public String getGuestName(){
        return guestName;
    }

    public String getGuestContact(){
        return guestContact;
    }
}
